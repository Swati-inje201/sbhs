		<h2>Welcome to Virtual Labs (SBHS)</h2>
		<p>
		The Virtual Labs Project was started as an initiative from the Ministry of Human
		Resource and Development (MHRD) to create online interactive media which would
		help students learn and experiment with difficult concepts in various domains. 
		As a part of this initiative, a virtual laboratory for Single Board Heater System
		(SBHS) has been developed. The objective of this lab is to help students, teachers 
		or in general enthusiasts perform experiments in the Instrumentation and Control System labs virtually, and yet have close to
		real life experience. The platform is focused on learning aspects as much as on 
		performing the experiments.
		</p>
		<h2>Key features of Virtual Labs</h2>
<p>
    <ul>
    <li>Here you can remotely access the Single Board Heater Systems (SBHS) hosted at IIT Bombay, and perform various experiments using it.<br /></li>
	<li>Single Board Heater system is a lab-in-a-box setup which is primarily used for teaching/studying 
	the theory of control systems. It can be thought of as a plant with a very small time constant 
	(of the order of 30s). You can remotely access this system and perform experiments on it - 
	from tests as simple as Step Tests to complicated closed loop tests!<br /></li>
	<li>Users are given two slots of 55 minute duration for performing the tests every day. They can 
	also make use of vacant current slots, if any, in addition to this. The relatively small time constant 
	of the SBHS allows the user to complete one or more experiments within the 55 minute duration.<br /></li>
	</ul>
</p>
