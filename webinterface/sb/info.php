		<h2>Welcome to Single Board Heater System Lab!</h2>
		<p>Welcome to SBHS Virtual Labs. Here you can remotely access the Single Board Heater Systems (SBHS) hosted at IIT Bombay, and perform various experiments using it.</p> 
<p>The virtual Lab for SBHS is made using free and open source software. PHP-mysql is used for web frontend and database. It targets at using Scilab at the client end to carry out various control experiments on  SBHS. Java application is used for communicating the data to and fro from the server and client. This Virtual lab interface is for those who want to access SBHS from outside IIT Bombay.</p> 

<p>We also have an internal virtual lab for SBHS at IIT Bombay where in people can access the lab over the intranet. This intranet lab was successfully used by Prof.Kannan Moudgalya for CL417- Process Control Course, CL692- Digital Control Course and by Prof. Kavi Arya for Embedded Systems Course. A good documentation of the hardware side of SBHS is also available at <a href="http://www.fossee.in/moodle">http://fossee.in/moodle</a>. We also have an active forum here so that your questions regarding this project are easily answered. Apart from this, we also provide you with scilab code of various control strategies implemented on SBHS. After getting a feel for SBHS using virtual labs, one can even purchase an SBHS and experiment with it locally. For purchasing SBHS, please visit <a href="http://www.nex-robotics.com/">http://www.nex-robotics.com/</a></p>
<p>
Here are the steps to be followed for using virtual labs.
<ol>
<li>From the Download Section, download and install Scilab.</li>
<li>Download and install Python 2.7.2</li>
<li>Register and book slot from this website</li>
<li>Run the python application for establishing communication between server and your computer</li>
<li>Launch Scilab, load the appropriate code and start experimenting remotely!</li>
</ol>
</p> 


	
				
